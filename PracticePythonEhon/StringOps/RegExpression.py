# examples of reg expression
print('examples of reg expression')
print('.: ', 'any one letter')
print('*: ', 'repeat previous letter 1-more times')
print('?: ', 'repeat previous letter 0 or 1 time')
print('{num}}: ', 'repeat the previous letter {num} times')
print('{numA, numB}}: ', 'repeat the previous letter more than {numA} times less than {numB}')
print('^: ', 'header of the line')
print('$: ', 'end of the line')
print('\: ', 'use the letter after \ as String')
print('[01234]: ', 'use the letter either 01234')
print('[^01234]: ', 'do not use the letter 01234')
print('[1-9]: ', 'use the letter 1-9')
print('(ab): ', 'use the group of letter ab. ex) ab, abab ...')
